template<typename T>
T power(T x,T n){
  T pro = 1;
  while(n!=0){
    if(n%2==1) pro *= x;
    x *= x;
    n /= 2;
  }
  return pro;
}

void solve() {
    int n, k;
    cin >> n >> k;
    vector<int> a(n),f(n+1),pre(n+1);
    int mval = 0;
    for (int i = 0; i < n; i++) {
        cin >> a[i];
        if (a[i] <= n) f[a[i]]++;
    }
    for (ll i = 1; i <=n; i++)
    {
        pre[i] = pre[i-1] + f[i];
    }

    
    ll ans = 1;
    for (int g = 1; g <= n; g++) {
        ll cnt = 0;
        ll limit = min(n,4*g-1);//couting the no >= 4g so subtract from total the 4g-1
        cnt += n - pre[limit];
        
        if(g<=n) cnt += f[g];
        if(2*g<=n) cnt+= f[2*g];
        if(3*g<=n) cnt+= f[3*g];
        if(cnt>= n-k){
            ans = g;
        }
        
    }
    cout << ans << "\n";
}


