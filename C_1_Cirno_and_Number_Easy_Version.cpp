template<typename T>
T mod(T a){ return (a<0)? -a : a; }

template<typename T>
T power(T x,T n){
  T pro = 1;
  while(n!=0){
    if(n%2==1) pro *= x;
    x *= x;
    n /= 2;
  }
  return pro;
}

ll fun(ll i, ll sum, ll a, ll d1, ll d2, ll maxi) {
    
    if (i == maxi) {
        return 2e18;
    }
    
    ll ans = 2e18;
   
    ll t1 = sum * 10 + d1;
    ll val1 = fun(i + 1, t1, a, d1, d2, maxi);
    ans = min({ans, abs(t1 - a), val1});
    
    
    ll t2 = sum * 10 + d2;
    ll val2 = fun(i + 1, t2, a, d1, d2, maxi);
    ans = min({ans, abs(t2 - a), val2});
    
    return ans;
}

